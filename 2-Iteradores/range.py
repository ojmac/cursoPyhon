#range(inicio, fin, incremento)

serie1 = range(5)   
serie2 = range(1, 5)    
serie3 = range(1, 5, 2)
print(serie1)  # range(0, 5)
print(serie2)  # range(5, 10)
print(serie3)  # range(1, 10, 2)

print(list(serie1))  # [0, 1, 2, 3, 4]
print(list(serie2))  # [5, 6, 7, 8]
print(list(serie3))  # [1, 3, 5, 7, 9]  




